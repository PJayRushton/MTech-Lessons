//: # Compound Assignment
//: A math shortcut

var stephCurrysPoints = 2
//: He drains a 3 (obviously)
stephCurrysPoints = stephCurrysPoints + 3
//: Use the compound assignment operator to shorten this action
stephCurrysPoints += 3
//: Ref reviews the play, his foot was on the line
stephCurrysPoints -= 1
//: Works for the * and / just the same

